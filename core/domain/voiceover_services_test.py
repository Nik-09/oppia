"""Tests for voiecover service methods."""

from __future__ import annotations

from core import feconf
from core.domain import state_domain
from core.domain import voiceover_services
from core.platform import models
from core.tests import test_utils


MYPY = False
if MYPY: # pragma: no cover
    from mypy_imports import voiceover_models


(voiceover_models,) = models.Registry.import_models([
    models.Names.VOICEOVER])


class EntityVoiceoverServicesTests(test_utils.GenericTestBase):
    def test_get_entity_voiceover_returns_correctly(self) -> None:
        dummy_manual_voiceover_dict: state_domain.VoiceoverDict = {
            'filename': 'filename1.mp3',
            'file_size_bytes': 3000,
            'needs_update': False,
            'duration_secs': 6.1
            }
        dummy_autogenerated_voiceover_dict: state_domain.VoiceoverDict = {
            'filename': 'filename2.mp3',
            'file_size_bytes': 3500,
            'needs_update': False,
            'duration_secs': 5.9
        }
        voiceover_models.EntityVoiceoversModel.create_new(
                feconf.ENTITY_TYPE_EXPLORATION, 'exp_id', 1, 'en-US', {
                    'content_0': {
                        'manual': dummy_manual_voiceover_dict,
                        'auto': dummy_autogenerated_voiceover_dict
                        }
                    }
        ).put()

        entity_voiceover = (
            voiceover_services.get_voiceovers_for_given_language_accent_code(
                'exploration', 'exp_id', 1, 'en-US'))

        self.assertEqual(entity_voiceover.entity_id, 'exp_id')
        self.assertEqual(entity_voiceover.entity_type, 'exploration')
        self.assertEqual(entity_voiceover.entity_version, 1)
        self.assertEqual(entity_voiceover.language_accent_code, 'en-US')
        self.assertDictEqual(
            entity_voiceover.voiceovers['content_0']['manual'].to_dict(),
            dummy_manual_voiceover_dict)
        self.assertDictEqual(
            entity_voiceover.voiceovers['content_0']['auto'].to_dict(),
            dummy_autogenerated_voiceover_dict)

    def test_get_entity_voiceover_creates_new_entity(self) -> None:
        entity_voiceover = (
            voiceover_services.get_voiceovers_for_given_language_accent_code(
                'exploration', 'exp_id', 1, 'en-US'))

        self.assertEqual(entity_voiceover.entity_id, 'exp_id')
        self.assertEqual(entity_voiceover.entity_type, 'exploration')
        self.assertEqual(entity_voiceover.entity_version, 1)
        self.assertEqual(entity_voiceover.language_accent_code, 'en-US')
        self.assertDictEqual(entity_voiceover.voiceovers, {})
